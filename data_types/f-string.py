#F-String Example

name = "John"
age = 26
salary = 8.7

print(f"Employee Name: {name}, Employee Age: {age}, Employee Salary: {salary}")

print(f"Employee Name: {name}, Employee Age: {age + 5}, Employee Salary: {salary}")

print(f"Employee Name: {name.upper()}, Employee Age: {age + 5}, Employee Salary: {salary}")
